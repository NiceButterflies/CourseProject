    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (priceList != null && schedule != null) {
            this.query.setIdClient(cl.getClientWhereLogin(login).getId());
            this.query.setIdProgramm(pl.getProgrammId((String) jComboBox1.getSelectedItem()));
            this.query.setIdSchedule(schedule.getId());
            this.query.setIdSubscription(priceList.getId());
            this.query.setStatus("на обработке");
            aql.saveQuery(query);
            JOptionPane.showMessageDialog(null, "Заявка отправлена.");
            refresh();
        } else {
            JOptionPane.showMessageDialog(null, "Выберите абонемент и время.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

