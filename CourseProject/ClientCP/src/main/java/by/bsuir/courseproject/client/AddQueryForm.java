    private void refresh() {
        priceListList = pll.getSubscriptionByIdProgramm(pl.getProgrammId((String) jComboBox1.getSelectedItem()));
        scheduleList = sl.getScheduleByIdProgramm(pl.getProgrammId((String) jComboBox1.getSelectedItem()));

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object[priceListList.size()][5],
                new String[]{
                    "#", "Тип", "Количество", "Стоимость", "Стоимость с учетом скидки"
                }
        ));
        jTable1.getColumnModel().getColumn(0).setMaxWidth(30);
        for (int j = 0; j < priceListList.size(); j++) {
            jTable1.getModel().setValueAt(j + 1, j, 0);
            jTable1.getModel().setValueAt(priceListList.get(j).getTypeSubscription(), j, 1);
            jTable1.getModel().setValueAt(priceListList.get(j).getDurationSubscription(), j, 2);
            jTable1.getModel().setValueAt(priceListList.get(j).getPrice(), j, 3);
            jTable1.getModel().setValueAt(getCost(j), j, 4);
            jTable1.setAutoCreateRowSorter(true);
        }

        jTable2.setModel(new javax.swing.table.DefaultTableModel(
                new Object[scheduleList.size()][4],
                new String[]{
                    "#", "День недели", "Время", "Инструктор"
                }
        ));
        jTable2.getColumnModel().getColumn(0).setMaxWidth(30);
        for (int j = 0; j < scheduleList.size(); j++) {
            jTable2.getModel().setValueAt(j + 1, j, 0);
            switch (scheduleList.get(j).getWeekday()) {
            case "1": jTable2.getModel().setValueAt("пн", j, 1); break;
                case "2": jTable2.getModel().setValueAt("вт", j, 1); break;
                    case "3": jTable2.getModel().setValueAt("ср", j, 1); break;
                        case "4": jTable2.getModel().setValueAt("чт", j, 1); break;
                            case "5": jTable2.getModel().setValueAt("пт", j, 1); break;
                                case "6": jTable2.getModel().setValueAt("сб", j, 1); break;
                            }
            jTable2.getModel().setValueAt(scheduleList.get(j).getTime(), j, 2);
            jTable2.getModel().setValueAt(il.getSurname(scheduleList.get(j).getIdInstructor()), j, 3);
            jTable2.setAutoCreateRowSorter(true);
        }

    }

    public int getCost(int j) {
        int cost = 0;
        return cost = priceListList.get(j).getPrice() - (priceListList.get(j).getPrice() * dl.getNumDiscount(cl.getClientWhereLogin(login).getIdDiscount()) / 100);
    }

