    private void checkRights() {
        if (!login.equals("admin")) {
            jButton1.setVisible(false);
            jButton2.setVisible(false);
            jButton3.setVisible(false);
        }
    }

    private void refresh() {
        discountList = dl.getAllDiscounts();

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                new Object[discountList.size()][3],
                new String[]{
                    "#", "Категория клиента", "Процент скидки"
                }
        ));
        jTable1.getColumnModel().getColumn(0).setMaxWidth(30);
        for (int j = 0; j < discountList.size(); j++) {
            jTable1.getModel().setValueAt(j + 1, j, 0);
            jTable1.getModel().setValueAt(discountList.get(j).getClientCategory(), j, 1);
            jTable1.getModel().setValueAt(discountList.get(j).getNumDiscount(), j, 2);
            jTable1.setAutoCreateRowSorter(true);
        }
    }

